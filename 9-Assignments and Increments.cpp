#include <iostream>

using namespace std;

int main()
{
	//Assignment
	
	int x=10;
	
	x+=5;
	cout << x << endl;
	x-=5;
	cout << x << endl;
	x*=5;
	cout << x << endl;
	x/=5;
	cout << x << endl;
	x%=5;
	cout << x << endl;
	
	//Increments
	
	int y=20;
	int z=20;
	
	cout << y++ << endl;
	cout << y << endl;
	
	cout << ++z << endl;
	cout << z << endl;
	
	
	
	
	return 0;
}
